#!/usr/bin/env node

import { writeFileSync, mkdirSync } from "node:fs";
import { join } from "node:path";

const outputDir = join(process.cwd(), "supabase");
mkdirSync(outputDir, { recursive: true });

const merchants = [
  "Chipotle",
  "Panda Express",
  "Potbelly",
  "Qdoba",
  "Wings Etc",
  "Panera",
  "Jersey Mike's",
  "Five Guys",
  "Jimmy John's",
  "Subway",
];

function sqlString(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

function buildClaimRow(index) {
  const merchant = merchants[index % merchants.length];
  const amount = (12 + (index % 13) * 0.95).toFixed(2);
  const submittedAt = new Date(
    Date.UTC(2026, 3, 3 + Math.floor(index / 6), 11 + (index % 8), (index * 7) % 60, 0),
  ).toISOString();

  let status = "auto_approved";
  let amountApproved = amount;
  let reviewerNotes = "null";

  if (index >= 38 && index < 44) {
    status = "flagged";
    amountApproved = "null";
  } else if (index >= 44 && index < 47) {
    status = "auto_rejected";
    amountApproved = "null";
    reviewerNotes = sqlString("Requested amount exceeds program cap.");
  } else if (index >= 49) {
    status = "manually_rejected";
    amountApproved = "null";
    reviewerNotes = sqlString("Receipt amount and requested amount do not align.");
  } else if (index >= 47) {
    status = "manually_approved";
  }

  const confidence =
    status === "auto_approved"
      ? (0.88 + (index % 6) * 0.01).toFixed(2)
      : status === "flagged"
        ? (0.58 + ((index - 38) % 5) * 0.03).toFixed(2)
        : status === "auto_rejected"
          ? (0.18 + (index % 3) * 0.04).toFixed(2)
          : status === "manually_approved"
            ? (0.69 + (index % 2) * 0.03).toFixed(2)
            : "0.41";

  return `(
  ${sqlString(`30000000-0000-0000-0000-${String(index + 1).padStart(12, "0")}`)},
  '22222222-2222-2222-2222-222222222222',
  ${sqlString(`nullifier-demo-${String(index + 1).padStart(3, "0")}`)},
  true,
  'orb',
  '${status}',
  ${amount},
  ${amountApproved},
  'food',
  ${sqlString(`${merchant} receipt submission ${index + 1}`)},
  ${sqlString(`claimant${index + 1}@demo.openclaims.app`)},
  ${confidence},
  ${sqlString(submittedAt)},
  ${sqlString(submittedAt)},
  ${reviewerNotes}
)`;
}

function buildAuditRow(index) {
  const claimId = `30000000-0000-0000-0000-${String(index + 1).padStart(12, "0")}`;
  let action = "auto_approved";
  let actor = "system";

  if (index >= 38 && index < 44) {
    action = "flagged";
  } else if (index >= 44 && index < 47) {
    action = "auto_rejected";
  } else if (index >= 47 && index < 49) {
    action = "manually_approved";
    actor = "11111111-1111-1111-1111-111111111111";
  } else if (index >= 49) {
    action = "manually_rejected";
    actor = "11111111-1111-1111-1111-111111111111";
  }

  return `(${sqlString(claimId)},'${action}',${sqlString(actor)},'{"seeded":true}'::jsonb)`;
}

const claimRows = Array.from({ length: 50 }, (_, index) => buildClaimRow(index)).join(",\n");
const auditRows = Array.from({ length: 50 }, (_, index) => buildAuditRow(index)).join(",\n");

const sql = `-- Demo seed generated by scripts/seed-demo-data.mjs
-- This seed assumes the schema from docs/01-technical-architecture.md already exists.

insert into public.organizers (id, email, name)
values ('11111111-1111-1111-1111-111111111111', 'demo@openclaims.app', 'Demo Organizer')
on conflict (id) do update set email = excluded.email, name = excluded.name;

insert into public.programs (
  id, name, slug, world_id_action, description, rules, budget_total, budget_committed, status, created_by
) values (
  '22222222-2222-2222-2222-222222222222',
  'Catapult Hacks 2026 Meal Stipend',
  'catapult-meal-stipend-2026',
  'openclaims-catapult-meal-stipend-2026',
  'Submit one meal receipt during Catapult Hacks 2026.',
  '{"max_amount_per_claim":25,"deadline":"2026-04-05T10:00:00.000Z","required_proof_types":["receipt"],"allowed_categories":["food"],"auto_approve_threshold":0.85}'::jsonb,
  1000,
  712,
  'active',
  '11111111-1111-1111-1111-111111111111'
)
on conflict (id) do update set
  name = excluded.name,
  slug = excluded.slug,
  world_id_action = excluded.world_id_action,
  description = excluded.description,
  rules = excluded.rules,
  budget_total = excluded.budget_total,
  budget_committed = excluded.budget_committed,
  status = excluded.status,
  created_by = excluded.created_by;

insert into public.claims (
  id, program_id, nullifier_hash, world_id_verified, verification_level, status,
  amount_requested, amount_approved, category, description, claim_contact_email, confidence_score, submitted_at, updated_at, reviewer_notes
) values
${claimRows}
on conflict (id) do nothing;

insert into public.audit_log (claim_id, action, actor, details)
values
${auditRows};
`;

const outputFile = join(outputDir, "seed.sql");
writeFileSync(outputFile, sql);

console.log(`Demo seed written to ${outputFile}`);
